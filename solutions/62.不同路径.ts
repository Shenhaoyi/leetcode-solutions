// @lc code=start
function uniquePaths(m: number, n: number): number {
  // dp表
  const dp = new Array(m).fill(undefined).map(() => new Array(n).fill(0));
  // 初始状态
  for (let i = 0; i < n; i++) {
    dp[0][i] = 1;
  }
  for (let i = 1; i < m; i++) {
    dp[i][0] = 1;
  }
  // 状态转移
  for (let i = 1; i < m; i++) {
    for (let j = 1; j < n; j++) {
      dp[i][j] = dp[i][j - 1] + dp[i - 1][j];
    }
  }
  return dp[m - 1][n - 1];
}
